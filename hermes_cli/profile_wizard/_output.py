def _description(spec: AgentProfileSpec) -> str:
    return (
        f"{spec.display_name}: {spec.primary_role.replace('_', ' ')} profile for "
        f"{spec.operating_context or 'Hermes'} with {spec.personality} personality, "
        f"{', '.join(spec.tones)} tone, and {spec.total_skills} selected skill(s)."
    )


def export_profile(spec: AgentProfileSpec, kind: str, output_dir: Path | None = None) -> Path | None:
    output_dir = output_dir or Path.cwd()
    output_dir.mkdir(parents=True, exist_ok=True)
    data = asdict(spec)
    if kind == "json":
        path = output_dir / f"{spec.profile_name}.profile.json"
        path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
        return path
    if kind == "js":
        path = output_dir / f"{spec.profile_name}.profile.js"
        path.write_text("export default " + json.dumps(data, indent=2, ensure_ascii=False) + ";\n", encoding="utf-8")
        return path
    if kind == "stdout":
        print(json.dumps(data, indent=2, ensure_ascii=False))
        return None
    return None
