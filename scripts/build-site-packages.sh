#!/usr/bin/env bash
# 在准备机上创建完整 Python venv，打包 site-packages 供离线安装
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
OUTPUT_DIR="${1:-$PROJECT_ROOT/hermes-offline-bundle}"
PYTHON_VERSION="${PYTHON_VERSION:-3.12}"
VENV_TMP="$OUTPUT_DIR/.venv-build"
SITE_TAR="$OUTPUT_DIR/python-site-packages.tar.gz"

echo "==> 创建临时 venv (Python $PYTHON_VERSION) ..."
rm -rf "$VENV_TMP"
uv venv "$VENV_TMP" --python "$PYTHON_VERSION"

export VIRTUAL_ENV="$VENV_TMP"
export UV_PYTHON="$VENV_TMP/bin/python"

echo "==> 安装 hermes-agent[all] 到 venv ..."
cd "$PROJECT_ROOT"
uv pip install -e ".[all]" 2>&1 | tail -5

echo "==> 安装 lazy deps 到 venv ..."
uv pip install \
    "anthropic==0.87.0" \
    "boto3==1.42.89" \
    "azure-identity==1.25.3" \
    "exa-py==2.10.2" \
    "firecrawl-py==4.17.0" \
    "parallel-web==0.4.2" \
    "mistralai==2.4.8" \
    "edge-tts==7.2.7" \
    "elevenlabs==1.59.0" \
    "faster-whisper==1.2.1" \
    "sounddevice==0.5.5" \
    "numpy==2.4.3" \
    "fal-client==0.13.1" \
    "honcho-ai==2.0.1" \
    "hindsight-client==0.6.1" \
    "python-telegram-bot[webhooks]==22.6" \
    "discord.py[voice]==2.7.1" \
    "brotlicffi==1.2.0.1" \
    "slack-bolt==1.27.0" \
    "slack-sdk==3.40.1" \
    "aiohttp==3.13.4" \
    "mautrix[encryption]==0.21.0" \
    "Markdown==3.10.2" \
    "aiosqlite==0.22.1" \
    "asyncpg==0.31.0" \
    "aiohttp-socks==0.11.0" \
    "dingtalk-stream==0.24.3" \
    "alibabacloud-dingtalk==2.2.42" \
    "qrcode==7.4.2" \
    "lark-oapi==1.5.3" \
    "defusedxml==0.7.1" \
    "modal==1.3.4" \
    "daytona==0.155.0" \
    "google-api-python-client==2.194.0" \
    "google-auth-oauthlib==1.3.1" \
    "google-auth-httplib2==0.3.1" \
    "youtube-transcript-api==1.2.4" \
    "agent-client-protocol==0.9.0" \
    2>&1 | tail -5

echo "==> 打包 site-packages ..."
SITE_DIR=$(find "$VENV_TMP" -type d -name "site-packages" | head -1)
if [[ -z "$SITE_DIR" ]]; then
    echo "[ERROR] 找不到 site-packages 目录"
    exit 1
fi

PKG_COUNT=$(find "$SITE_DIR" -maxdepth 1 -type d | wc -l)
echo "    $PKG_COUNT 个包，来自 $SITE_DIR"

cd "$SITE_DIR"
tar -czf "$SITE_TAR" .
echo "    打包完成: $(du -m "$SITE_TAR" | cut -f1) MB"

# 清理临时 venv
rm -rf "$VENV_TMP"

echo ""
echo "==> 完成！"
echo "    $SITE_TAR"
echo "    传输到目标机后解压到 venv 的 site-packages 目录即可"
