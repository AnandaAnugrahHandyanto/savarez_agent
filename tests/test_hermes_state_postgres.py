def test_postgres_schema_sql_contains_core_tables_and_current_version():
    for table in ("schema_version", "sessions", "messages", "state_meta", "compression_locks"):
        assert f"CREATE TABLE IF NOT EXISTS {table}" in POSTGRES_SCHEMA_SQL
    assert str(SCHEMA_VERSION) in POSTGRES_SCHEMA_SQL


def test_postgres_schema_sql_avoids_sqlite_only_syntax():
    lowered = POSTGRES_SCHEMA_SQL.lower()

    assert "autoincrement" not in lowered
    assert "pragma" not in lowered
    assert "fts5" not in lowered
    assert "create virtual table" not in lowered
    assert "insert or ignore" not in lowered
    assert "generated by default as identity" in lowered
    assert "on conflict" in lowered


def test_plan_postgres_migrations_are_ordered_and_contiguous():
    migrations = plan_postgres_migrations(0)
    versions = [migration.version for migration in migrations]

    assert versions == list(range(1, SCHEMA_VERSION + 1))
    assert len(versions) == len(set(versions))


def test_plan_postgres_migrations_current_version_is_noop():
    assert plan_postgres_migrations(SCHEMA_VERSION) == []


def test_plan_postgres_migrations_refuses_future_schema_version():
    with pytest.raises(ValueError, match="newer than this Hermes"):
        plan_postgres_migrations(SCHEMA_VERSION + 1)


def test_postgres_cursor_translates_sqlite_placeholders_and_insert_ignore():
    class FakeCursor:
        description = None

        def __init__(self):
            self.calls = []

        def execute(self, sql, params=None):
            self.calls.append((sql, params))

    fake = FakeCursor()
    cursor = _PostgresCursor(fake)

    cursor.execute(
        "INSERT OR IGNORE INTO sessions (id, source) VALUES (?, ?)",
        ("s1", "cli"),
    )

    assert fake.calls == [
        (
            "INSERT INTO sessions (id, source) VALUES (%s, %s) ON CONFLICT DO NOTHING",
            ("s1", "cli"),
        )
    ]


