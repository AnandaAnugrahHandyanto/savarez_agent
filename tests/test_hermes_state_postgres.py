def test_export_import_preserves_session_messages_and_inactive_rows(tmp_path):
    source = SessionDB(tmp_path / "source.db")
    target = SessionDB(tmp_path / "target.db")
    try:
        source.create_session("s1", "cli", model="m", cwd="/workspace")
        first_id = source.append_message("s1", "user", "hello")
        second_id = source.append_message("s1", "assistant", "world")
        source._conn.execute("UPDATE messages SET active = 0 WHERE id = ?", (second_id,))
        source._conn.commit()

        exported = source.export_session("s1", include_inactive=True)
        target.import_session(exported)

        assert target.get_session("s1")["model"] == "m"
        imported_messages = target.get_messages("s1", include_inactive=True)
        assert [msg["id"] for msg in imported_messages] == [first_id, second_id]
        assert [msg["content"] for msg in imported_messages] == ["hello", "world"]
        assert imported_messages[1]["active"] == 0
    finally:
        source.close()
        target.close()


def test_structured_content_encoding_is_postgres_text_safe():
    encoded = SessionDB._encode_content({"parts": ["image", "text"]})

    assert "\x00" not in encoded
    assert SessionDB._decode_content(encoded) == {"parts": ["image", "text"]}
    assert SessionDB._decode_content("\x00json:{\"legacy\": true}") == {"legacy": True}


def test_structured_content_encoding_preserves_literal_prefix_strings():
    literal = '__hermes_json__:["plain", "text"]'

    encoded = SessionDB._encode_content(literal)

    assert encoded != literal
    assert SessionDB._decode_content(encoded) == literal


def test_postgres_cursor_translates_sqlite_blob_newline_literals():
    class FakeCursor:
        description = None

        def __init__(self):
            self.calls = []

        def execute(self, sql, params=None):
            self.calls.append((sql, params))

    fake = FakeCursor()
    _PostgresCursor(fake).execute("SELECT REPLACE(content, X'0A', ' '), X'0D' FROM messages")

    assert fake.calls == [("SELECT REPLACE(content, chr(10), ' '), chr(13) FROM messages", None)]


def test_search_context_helper_removes_full_content(tmp_path):
    db = SessionDB(tmp_path / "search-context.db")
    try:
        db.create_session("s1", "cli")
        db.append_message("s1", "user", "before")
        match_id = db.append_message("s1", "assistant", "secret full content")
        db.append_message("s1", "user", "after")
        matches = [{"id": match_id, "content": "secret full content", "snippet": "secret"}]

        db._add_search_context(matches)

        assert "content" not in matches[0]
        assert matches[0]["context"] == [
            {"role": "user", "content": "before"},
            {"role": "assistant", "content": "secret full content"},
            {"role": "user", "content": "after"},
        ]
    finally:
        db.close()


def test_import_sessions_orders_parents_before_children(tmp_path):
    source = SessionDB(tmp_path / "source-lineage.db")
    target = SessionDB(tmp_path / "target-lineage.db")
    try:
        source.create_session("parent", "cli")
        source.create_session("child", "cli", parent_session_id="parent")
        child = source.export_session("child")
        parent = source.export_session("parent")

        assert target.import_sessions([child, parent]) == 2

        assert target.get_session("child")["parent_session_id"] == "parent"
    finally:
        source.close()
        target.close()


