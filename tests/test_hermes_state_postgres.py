def test_migrate_source_iterator_streams_active_messages(tmp_path):
    from hermes_cli.migrate import (
        _iter_source_sessions,
        _source_message_count,
        _source_session_count,
    )

    source = SessionDB(tmp_path / "stream-source.db")
    try:
        source.create_session("parent", "cli")
        source.create_session("child", "cli", parent_session_id="parent")
        source.append_message("child", "user", "active message")
        inactive_message_id = source.append_message("child", "assistant", "inactive message")
        source._conn.execute("UPDATE messages SET active = 0 WHERE id = ?", (inactive_message_id,))
        source._conn.commit()

        streamed = list(_iter_source_sessions(source, include_inactive=False))

        assert _source_session_count(source, include_inactive=False) == 2
        assert _source_message_count(source, include_inactive=False) == 1
        assert [session["id"] for session in streamed] == ["parent", "child"]
        assert streamed[1]["parent_session_id"] == "parent"
        assert [message["content"] for message in streamed[1]["messages"]] == ["active message"]
    finally:
        source.close()


def test_redact_dsn_hides_url_passwords():
    from hermes_cli.migrate import _redact_dsn

    redacted = _redact_dsn(
        "postgresql://user:password123@example/db?sslpassword=cert-secret&connect_timeout=5"
    )

    assert "password123" not in redacted
    assert "cert-secret" not in redacted
    assert redacted == "postgresql://user:***@example/db?sslpassword=%2A%2A%2A&connect_timeout=5"


def test_redact_dsn_hides_keyword_passwords():
    from hermes_cli.migrate import _redact_dsn

    redacted = _redact_dsn(
        "host=db.example dbname=hermes user=hermes password='p w' passfile=/tmp/pgpass"
    )

    assert "p w" not in redacted
    assert "/tmp/pgpass" not in redacted
    assert redacted == "host=db.example dbname=hermes user=hermes password=*** passfile=***"


def test_redact_dsn_hides_escaped_keyword_passwords():
    from hermes_cli.migrate import _redact_dsn

    redacted = _redact_dsn(r"host=db password='abc\'def' user=hermes")

    assert "abc" not in redacted
    assert "def" not in redacted
    assert redacted == "host=db password=*** user=hermes"
