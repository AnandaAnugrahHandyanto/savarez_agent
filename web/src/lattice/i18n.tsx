export const LangContext = createContext<string>('en');

export function useT(): (key: string) => string {
  const lang = useContext(LangContext);
  return (key: string) => {
    const dict = STRINGS[lang] || STRINGS.en;
    return dict[key] || STRINGS.en[key] || key;
  };
}

